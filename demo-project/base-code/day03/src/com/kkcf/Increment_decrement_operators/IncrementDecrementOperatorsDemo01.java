package com.kkcf.Increment_decrement_operators;

public class IncrementDecrementOperatorsDemo01 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        int c = 20;

        System.out.println(a == b); // true
        System.out.println(a == c); // false
    }
}
